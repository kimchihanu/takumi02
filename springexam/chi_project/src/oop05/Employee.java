package oop05;

public class Employee {
	private String no;
	private String name;
	private Integer salary;
	public String getNo() {
	return no;
	}
	public void setNo(String no) {
	this.no = no;
	}
	public String getName() {
	return name;
	}
	public void setName(String name) {
	this.name = name;
	}
	public Integer getSalary() {
	return salary;
	}
	public void setSalary(Integer salary) {
	this.salary = salary;
	}
}
